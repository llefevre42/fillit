#include "fillit.h"

char	**ft_split_pieces(char *in)
{
	int i;

	i = 1;
	while (in[i] != '\0')
	{
		if (in[i] == '\n' && in[i - 1] == '\n')
			in[i] = 'X';
		i++;
	}
	return (ft_strsplit(in, 'X'));
}
