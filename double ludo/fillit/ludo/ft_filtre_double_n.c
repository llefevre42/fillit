#include "fillit.h"

char	**ft_filtre_double_n(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '\n' && str[i - 1] == '\n')
			str[i] = 'X';
		i++;
	}
	return (ft_strsplit(str, 'X'));
}
