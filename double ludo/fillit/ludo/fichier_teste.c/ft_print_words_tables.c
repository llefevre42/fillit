void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	while (*str)
		ft_putchar(*str++);
	ft_putchar('\n');
}

void	ft_print_words_tables(char **tab)
{
	while (*tab)
		ft_putstr(*tab++);
}
